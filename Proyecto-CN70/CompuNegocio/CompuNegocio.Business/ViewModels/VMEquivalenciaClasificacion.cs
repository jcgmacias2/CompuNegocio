using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Aprovi.Business.ViewModels
{
    public class VMEquivalenciaClasificacion
    {
        public VMEquivalenciaClasificacion() { }

        public VMEquivalenciaClasificacion(string clave, string descripcion)
